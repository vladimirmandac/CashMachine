    private void jButtonSendPswActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendPswActionPerformed
        // TODO add your handling code here:
        
        String emailFrom, emailPsw;
        
        String username, loginEmail, forgotPsw;
        
        try {
            Connection connection = SingletonConnection.getConnection();
            Statement statement = connection.createStatement();
            
            
            String queryString1 = ("SELECT `email`, `emailpassword` FROM admins");
            ResultSet rs1 = statement.executeQuery(queryString1);

            rs1.first();
            emailFrom = rs1.getString(1);
            emailPsw = rs1.getString(2);
                
            String queryString2 = ("SELECT `username`, `loginemail`, `password` FROM users");
            ResultSet rs2 = statement.executeQuery(queryString2);
            
            while (rs2.next()) {                
                username = rs2.getString(1);
                loginEmail = rs2.getString(2);
                forgotPsw = rs2.getString(3);
            }
                
        } catch (SQLException ex) {
            System.out.println(ex);
            jLabelErrorMsg.setText("There was an error, please try again.");
        }
    }//GEN-LAST:event_jButtonSendPswActionPerformed

